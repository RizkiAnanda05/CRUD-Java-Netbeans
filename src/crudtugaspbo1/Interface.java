    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
       
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/Mahasiswa", "root","");
            con.createStatement().executeUpdate("update datamhs set Nama_MHS = '"+Nama_Mhs.getText()+"', EMAIL_MHS = '"+Email_mhs.getText()+"' where Nim_MHS = '"+Nim_mhs.getText()+"'  ");
            tampilkan();
            reset();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Nim Tidak Dapat Diubah!!");
        }
         
    }                                        

