    private void tampilkan(){
        int row = table1.getRowCount();
        for (int a = 0; a < row ; a++){
            model.removeRow(0);
        }
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/Mahasiswa", "root","");
            ResultSet rs = con.createStatement().executeQuery("SELECT * FROM datamhs " );
            while (rs.next()){
                String data [] = {rs.getString(1),rs.getString(2),rs.getString(3)};
                model.addRow(data); 
            }
        }catch(SQLException ex ){
            Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null,ex );
        }
        
        
    }

